void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
  if(hi2c->Instance == I2C1)
  {
    uI2C1AddrMatchCount++;

    if(TransferDirection == I2C_DIRECTION_TRANSMIT) //slave receive mode
    {
      if(HAL_I2C_Slave_Sequential_Receive_IT(&hi2c1, (uint8_t *)aSlaveRxBuffer, RXBUFFERSIZE, I2C_NEXT_FRAME) != HAL_OK)
      {
        Error_Handler();
      }
    }
    else if(TransferDirection == I2C_DIRECTION_RECEIVE) //slave trasfer mode
    {
      switch(aSlaveRxBuffer[0])
      {
        case 0x01:
          if(HAL_I2C_Slave_Sequential_Transmit_IT(&hi2c1, (uint8_t *)aSlaveTxBuffer, TXBUFFERSIZE, I2C_LAST_FRAME) != HAL_OK)
          {
            Error_Handler();
          }
          break;

        default:
          break;
      }
    }
  }
}

